#!/usr/bin/env python3
"""
Consult Anthropic Claude API for Silicon Valley-level website design recommendations
for SEER AI Trading Platform
"""

import os
import anthropic

client = anthropic.Anthropic(api_key=os.environ.get("ANTHROPIC_API_KEY"))

DESIGN_PROMPT = """You are a world-class UI/UX designer who has worked at top Silicon Valley companies like Apple, Tesla, SpaceX, and OpenAI. You specialize in creating futuristic, sci-fi, next-generation website designs for AI-powered platforms.

I need you to design a marketing website for SEER - an AI-powered autonomous crypto trading platform. The platform uses 11 specialized AI agents that work 24/7 to analyze markets and execute trades automatically.

**Brand Identity:**
- Name: SEER (like a prophet who can see the future)
- Industry: AI-powered cryptocurrency trading
- Target: Both institutional and retail traders
- Personality: Futuristic, intelligent, trustworthy, cutting-edge

**Requirements:**
1. The design must look like it was built by a top Silicon Valley AI company
2. Futuristic, sci-fi aesthetic with dark theme
3. Purple/blue gradient accents (existing brand colors)
4. Animations: particle effects, glowing elements, smooth transitions
5. The logo should represent prediction/foresight + trading/finance
6. Must feel innovative, next-gen, and AI-driven

**Pages to Design:**
1. Landing Page (hero, stats, features, how it works, CTA)
2. Features Page (platform capabilities)
3. AI Agents Page (showcase 11 agents)
4. Pricing Page (3 tiers with waitlist)
5. About Page (founded 2025, vision, roadmap)

Please provide:

1. **Logo Concept**: Describe an SVG logo design that represents SEER (prediction + trading). Include specific SVG path suggestions or geometric shapes.

2. **Color Palette**: Exact hex codes for primary, secondary, accent, and gradient colors that create a futuristic feel.

3. **Typography**: Font recommendations (Google Fonts) for headings and body text.

4. **Animation Ideas**: Specific CSS/JS animations for:
   - Hero section background (particles, grid, waves)
   - Card hover effects
   - Section transitions
   - Loading states
   - Glowing/pulsing elements

5. **Landing Page Layout**: Detailed section-by-section design with:
   - Hero: What elements, animations, CTAs
   - Stats: How to display live metrics futuristically
   - Features: Card design, icons, layout
   - How it works: Visual flow/timeline
   - Social proof: Testimonial design

6. **UI Components**: Design specifications for:
   - Buttons (primary, secondary, ghost)
   - Cards (feature cards, pricing cards, agent cards)
   - Navigation (header, mobile menu)
   - Footer

7. **Sci-Fi Elements**: Specific design elements that make it feel futuristic:
   - Grid patterns
   - Glowing borders
   - Holographic effects
   - Data visualization aesthetics
   - Cyber/tech patterns

Be extremely specific with CSS classes, Tailwind utilities, and implementation details. Think like you're designing for SpaceX or Tesla's website - clean, futuristic, premium."""

def get_design_recommendations():
    """Get design recommendations from Claude AI"""
    
    message = client.messages.create(
        model="claude-sonnet-4-20250514",
        max_tokens=8000,
        messages=[
            {"role": "user", "content": DESIGN_PROMPT}
        ]
    )
    
    return message.content[0].text

if __name__ == "__main__":
    print("=" * 80)
    print("SEER MARKETING WEBSITE - SILICON VALLEY DESIGN CONSULTATION")
    print("=" * 80)
    print()
    
    recommendations = get_design_recommendations()
    
    # Save to file
    with open("/home/ubuntu/seer/design_recommendations.md", "w") as f:
        f.write("# SEER Marketing Website - Design Recommendations\n\n")
        f.write("*Generated by Claude AI - Silicon Valley Design Consultation*\n\n")
        f.write(recommendations)
    
    print(recommendations)
    print()
    print("=" * 80)
    print("Design recommendations saved to /home/ubuntu/seer/design_recommendations.md")
    print("=" * 80)
